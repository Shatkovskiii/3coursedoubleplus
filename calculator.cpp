#include <iostream>

using namespace std;

int main() {
    int a, b;  // объявляем две переменные для чисел
    char sign;   // объявляем переменную для хранения операции

    // пользователь вводит первое число операцию и второе число
    cin >> a >> sign >> b;

    // проверяем какая операция введена
    if (sign == '+') {
        // если операция сложения выводим результат сложения
        cout << a + b;
    } else if (sign == '-') {
        // если операция вычитания выводим результат вычитания
        cout << a - b;
    } else if (sign == '*') {
        // если операция умножения выводим результат умножения
        cout << a * b;
    } else if (sign == '/') {
        // если операция деления проверяем что делитель не равен нулю
        if (b != 0) {
            // если делитель не ноль выводим результат деления
            cout << a / b;
        } else {
            // если делитель равен нулю выводим сообщение об ошибке
            cout << "деление на ноль!";
        }
    } else {
        // если введена неизвестная операция выводим сообщение об ошибке
        cout << "неверная операция!";
    }

    // завершаем выполнение программы
    return 0;
}
